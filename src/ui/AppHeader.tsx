/**
 * Formats the header string for testing purposes.
 * Example: `в•ёANVER CODEв•єв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ qwen3.6-plus в”Ѓв”Ѓ tokens: 1.5k`
 */
export function formatHeader(model: string, tokenCount: number): string {
  const tokens = tokenCount >= 1000
    ? `${(tokenCount / 1000).toFixed(1)}k`
    : String(tokenCount);

  return `в•ёANVER CODEв•єв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ ${model} в”Ѓв”Ѓ tokens: ${tokens}`;
}

// в”Ђв”Ђ Component в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

interface AppHeaderProps {
  model: string;
  tokenCount: number;
}

export function AppHeader({ model, tokenCount }: AppHeaderProps) {
  const tokens = tokenCount >= 1000
    ? `${(tokenCount / 1000).toFixed(1)}k`
    : String(tokenCount);

  return (
    <Box marginBottom={1}>
      <Text>
        {theme.primary('в•ёANVER CODEв•є')}
        {theme.dim('в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ ')}
        {theme.secondary(model)}
        {theme.dim(' в”Ѓв”Ѓ tokens: ')}
        {theme.secondary(tokens)}
      </Text>
    </Box>
  );
}
